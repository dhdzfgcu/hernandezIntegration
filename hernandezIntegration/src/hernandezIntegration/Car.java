package hernandezIntegration;

public class Car {
private String model;
private String make;
private int year;
public void setModel(String setModel) {
  setModel = model;
}
public void setMake(String setMake) {
  setMake = make;
}
public void setYear(int setYear) {
  setYear = year;
}
public String getModel(String getModel) {
  return model;
}
public String getMake(String getMake) {
  return make;
}
public int getYear(int getYear) {
  return year;
}
}
