public class Spaghetti {
  int length;
  int width;
public Spaghetti(int pastaLength, int pastaWidth) {
  setLength(pastaLength);
  setWidth(pastaWidth);
}
int getLength() {
 return length;
}
public void setLength(int length) {
  this.length = length;
}
public int getWidth() {
  return width;
}
public void setWidth(int width) {
  this.width = width;
}

}
